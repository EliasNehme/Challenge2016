package smlms.sample;

public class Node {
	public double x;
	public double y;
	public double z;
	public double radius;
	public double thickness;
	
	public Node(double x, double y, double z, double radius, double thickness) {
		this.x = x;
		this.y = y;
		this.z = z;
		this.radius = radius;
		this.thickness = thickness;
	}
}
